# 24 часа
# 0-4 доброй ночи
# 4-11 доброе утро
# 12-16 добрый день
# 16-0 доброй вечер

for i in range (0, 24):
    if 0 <= i < 4:
        print(f'На часах: {i}:00 Доброй ночи!')
    elif 4 <= i <= 11:
        print(f'На часах: {i}:00 Доброе утро')
    elif 12 <= i < 16:
        print(f'На часах: {i}:00 Добрый день')
    else:
        print(f'На часах: {i}:00 Добрый вечер')

